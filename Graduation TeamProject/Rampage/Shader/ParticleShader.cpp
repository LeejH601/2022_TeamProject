D3D12_STREAM_OUTPUT_DESC CParticleShader::CreateStreamOuputState(int nPipelineState)
{
	D3D12_STREAM_OUTPUT_DESC d3dStreamOutputDesc;
	::ZeroMemory(&d3dStreamOutputDesc, sizeof(D3D12_STREAM_OUTPUT_DESC));

	if (nPipelineState == 0)
	{
		UINT nStreamOutputDecls = 12;
		D3D12_SO_DECLARATION_ENTRY* pd3dStreamOutputDecls = new D3D12_SO_DECLARATION_ENTRY[nStreamOutputDecls];
		pd3dStreamOutputDecls[0] = { 0, "POSITION", 0, 0, 3, 0 };
		pd3dStreamOutputDecls[1] = { 0, "VELOCITY", 0, 0, 3, 0 };
		pd3dStreamOutputDecls[2] = { 0, "LIFETIME", 0, 0, 1, 0 };
		pd3dStreamOutputDecls[3] = { 0, "TYPE", 0, 0, 1, 0 };
		pd3dStreamOutputDecls[4] = { 0, "EMITTIME", 0, 0, 1, 0 };
		pd3dStreamOutputDecls[5] = { 0, "TEXTUREINDEX", 0, 0, 1, 0 };
		pd3dStreamOutputDecls[6] = { 0, "TEXTURECOORD", 0, 0, 2, 0 };
		pd3dStreamOutputDecls[7] = { 0, "SIZE", 0, 0, 2, 0 };
		pd3dStreamOutputDecls[8] = { 0, "EMISSIVE", 0, 0, 1, 0 };
		pd3dStreamOutputDecls[9] = { 0, "ROTATEFLAG", 0, 0, 1, 0 };
		pd3dStreamOutputDecls[10] = { 0, "SCALEFLAG", 0, 0, 1, 0 };
		pd3dStreamOutputDecls[11] = { 0, "COLOR", 0, 0, 4, 0 };
		//float EmitTime : EMITTIME; // 방출 시작 시간
		UINT* pBufferStrides = new UINT[1];
		pBufferStrides[0] = sizeof(CParticleVertex);

		d3dStreamOutputDesc.NumEntries = nStreamOutputDecls;
		d3dStreamOutputDesc.pSODeclaration = pd3dStreamOutputDecls;
		d3dStreamOutputDesc.NumStrides = 1;
		d3dStreamOutputDesc.pBufferStrides = pBufferStrides;
		d3dStreamOutputDesc.RasterizedStream = D3D12_SO_NO_RASTERIZED_STREAM;
	}

	return(d3dStreamOutputDesc);
}

void CParticleShader::CreateGraphicsPipelineState(ID3D12Device* pd3dDevice, ID3D12RootSignature* pd3dGraphicsRootSignature, int nPipelineState)
{
	m_nPipelineStates = 4;
	m_ppd3dPipelineStates.resize(m_nPipelineStates);
	

	DXGI_FORMAT pdxgiRtvFormat = DXGI_FORMAT_UNKNOWN;
	DXGI_FORMAT pdxgiRtvFormats[7] = { DXGI_FORMAT_R8G8B8A8_UNORM, DXGI_FORMAT_R16G16B16A16_FLOAT, DXGI_FORMAT_R16G16B16A16_UNORM, DXGI_FORMAT_R32G32B32A32_FLOAT,
		DXGI_FORMAT_R16G16B16A16_FLOAT, DXGI_FORMAT_R16G16B16A16_FLOAT, DXGI_FORMAT_R32_FLOAT };

	CParticleShader::CreateShader(pd3dDevice, pd3dGraphicsRootSignature, 0, &pdxgiRtvFormat, 0); //Stream Output Pipeline State // DXGI_FORMAT_UNKNOWN
	CParticleShader::CreateShader(pd3dDevice, pd3dGraphicsRootSignature, 7, pdxgiRtvFormats, 1); //Draw Pipeline State // DXGI_FORMAT_R8G8B8A8_UNORM

	CParticleShader::CreateShader(pd3dDevice, pd3dGraphicsRootSignature, 0, &pdxgiRtvFormat, 1, FALSE); //Stream Output Pipeline State // DXGI_FORMAT_UNKNOWN
	CParticleShader::CreateShader(pd3dDevice, pd3dGraphicsRootSignature, 7, pdxgiRtvFormats, 2, FALSE); //Draw Pipeline State - NoDepth(땅 위에 이펙트 전용)
}

void CParticleShader::OnPrepareRender(ID3D12GraphicsCommandList* pd3dCommandList, int nPipelineState)
{
	if (m_ppd3dPipelineStates.data() && m_ppd3dPipelineStates[nPipelineState]) pd3dCommandList->SetPipelineState(m_ppd3dPipelineStates[nPipelineState].Get());

	UpdateShaderVariables(pd3dCommandList);
}

